export const permissionSchema = z.object({
  _id: z.string(),
  name: z.string(),
  route: z.string(),
  method: z.string(),
  requiresAuth: z.boolean(),
  createdAt: z.date(),
  updatedAt: z.date(),
  _v: z.number().optional(),
});

function DragHandle({ id }: { id: string }) {
  const { attributes, listeners } = useSortable({
    id,
  });

  return (
    <Button
      {...attributes}
      {...listeners}
      variant="ghost"
      size="icon"
      className="size-7 text-muted-foreground hover:bg-transparent"
    >
      <GripVerticalIcon className="size-3 text-muted-foreground" />
      <span className="sr-only">Drag to reorder</span>
    </Button>
  );
}

export const columns: ColumnDef<z.infer<typeof permissionSchema>>[] = [
  {
    id: "drag",
    header: () => null,
    cell: ({ row }) => <DragHandle id={row.original._id} />,
  },
  {
    id: "select",
    header: ({ table }) => (
      <div className="flex items-center justify-center">
        <Checkbox
          checked={
            table.getIsAllPageRowsSelected() ||
            (table.getIsSomePageRowsSelected() && "indeterminate")
          }
          onCheckedChange={(value) => table.toggleAllPageRowsSelected(!!value)}
          aria-label="Select all"
        />
      </div>
    ),
    cell: ({ row }) => (
      <div className="flex items-center justify-center">
        <Checkbox
          checked={row.getIsSelected()}
          onCheckedChange={(value) => row.toggleSelected(!!value)}
          aria-label="Select row"
        />
      </div>
    ),
    enableSorting: false,
    enableHiding: false,
  },
  {
    accessorKey: "name",
    header: "Permission",
    cell: ({ row }) => {
      return <TableCellViewer item={row.original} />;
    },
    enableHiding: false,
  },
  {
    accessorKey: "method",
    header: "Method",
    cell: ({ row }) => {
      const method = row.original.method?.toUpperCase();

      const methodColorMap: Record<string, string> = {
        GET: "border-green-500 text-green-600",
        POST: "border-blue-500 text-blue-600",
        PUT: "border-yellow-500 text-yellow-600",
        DELETE: "border-red-500 text-red-600",
        PATCH: "border-purple-500 text-purple-600",
      };

      const className =
        methodColorMap[method] || "border-gray-400 text-gray-500";

      return (
        <div className="w-32">
          <Badge variant="outline" className={`px-1.5 ${className}`}>
            {method}
          </Badge>
        </div>
      );
    },
  },
  {
    accessorKey: "requireAuth",
    header: "Require Auth",
    cell: ({ row }) => (
      <Badge
        variant="outline"
        className="flex gap-1 px-1.5 text-muted-foreground [&_svg]:size-3"
      >
        {row.original.requiresAuth ? (
          <CheckCircle2Icon className="text-green-500 dark:text-green-400" />
        ) : (
          <ShieldX />
        )}
        {row.original.requiresAuth ? "True" : "False"}
      </Badge>
    ),
  },
  {
    accessorKey: "route",
    header: "Route",
    cell: ({ row }) => {
      return row.original.route;
    },
  },
  {
    accessorKey: "createdAt",
    header: "Created Date",
    cell: ({ row }) => {
      const date = new Date(row.original.createdAt);
      return new Intl.DateTimeFormat("en-US", {
        month: "short",
        day: "2-digit",
        year: "numeric",
      }).format(date); // âžœ "Jul 25, 2025"
    },
  },
  {
    id: "actions",
    cell: (info) => {
      const row = info.row;
      const [isEditOpen, setIsEditOpen] = useState(false);
      const permission = row.original;

      return (
        <DropdownMenu>
          <DropdownMenuTrigger asChild>
            <Button
              variant="ghost"
              className="flex size-8 text-muted-foreground data-[state=open]:bg-muted"
              size="icon"
            >
              <MoreVerticalIcon />
              <span className="sr-only">Open menu</span>
            </Button>
          </DropdownMenuTrigger>
          <DropdownMenuContent align="end" className="w-32">
            <DropdownMenuItem
              onSelect={(e) => {
                e.preventDefault();
                queueMicrotask(() => setIsEditOpen(true));
              }}
            >
              <FilePen className="mr-2 h-4 w-4" />
              Edit
            </DropdownMenuItem>

            <UpdatePermission
              permission={permission}
              open={isEditOpen}
              onOpenChange={setIsEditOpen}
            />

            <DropdownMenuSeparator />
            <DeletePermission permissionId={row.original._id} />
          </DropdownMenuContent>
        </DropdownMenu>
      );
    },
  },
];

